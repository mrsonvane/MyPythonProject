a="Hello world"
print(a[ : 7])            #Slice from start to 7, in this case 7 place char will not disply
print(a[ : ])             # Slice from start to end all letters
print(a[2: ])             # Secod position to end
print(a[3:8])             #From position 3 to 8 in this case 8th position letter will not disply
print(a[:-1])             # skip last letter and disply all
print(a[-5:-1])